#include "Task.h"

using namespace std;

/***************************************************/

Task::Task(int i, int r, int d)
{ 
	id = i;
	requestTime = r;
	duration = d;
	startTime = -1;
	
} /* constructer */

/***************************************************/

Task::Task(int i, int r, int d, int s)
{ 
	id = i;
	requestTime = r;
	duration = d;
	startTime = s;
	
} /* constructer */

/***************************************************/

Task::Task() 
{ 
	id = requestTime = duration = startTime = -1;
	
} /* constructer -- generic */

/***************************************************/

Task::~Task(){}

int	Task::getDuration() const { return duration; }
int Task::getId() const { return id; }
int Task::getRequestTime() const { return requestTime; }
int Task::getStartTime() const { return startTime; }

/***************************************************/

void Task::setStartTime(int t) { startTime = t; }

/***************************************************/

int Task::calculateWaitingTime()
{
	return startTime - requestTime;
} /* calculate waiting time */

